function getBibleRef(ref) {
  // Ex: "Sòm 23:1-6" → Psaumes ch23 v1-6
  const mapLivre = {
    'Jen': 'Genèse', 'Egz': 'Exode', 'Lev': 'Lévitique',
    'Nom': 'Nombres', 'Dèt': 'Deutéronome', 'Joz': 'Josué',
    'Jij': 'Juges', 'Rout': 'Ruth', '1Sm': '1 Samuel',
    '2Sm': '2 Samuel', '1Wa': '1 Rois', '2Wa': '2 Rois',
    'Sòm': 'Psaumes', 'Pwov': 'Proverbes', 'Eza': 'Ésaïe',
    'Jer': 'Jérémie', 'Mat': 'Matthieu', 'Jan': 'Jean',
    'Wom': 'Romains', '1Ko': '1 Corinthiens', 'Efe': 'Éphésiens',
    'Fil': 'Philippiens', 'Ebre': 'Hébreux', 'Apo': 'Apocalypse'
  };

  const parts = ref.split(' ');
  const abrev = parts[0];
  const livre = mapLivre[abrev] || abrev;
  if (!parts[1]) return { livre, chap: 1, versets: {} };

  const chapVers = parts[1].split(':');
  const chap = parseInt(chapVers[0]);
  const book = BIBLE_FR[livre];
  if (!book) return { livre, chap, versets: {}, manque: true };
  const ch = book[chap];
  if (!ch) return { livre, chap, versets: {}, manque: true };
  return { livre, chap, versets: ch };
}
